public class CharDataType {
    public static void main(String[] args){
        // Char data type use '' or single quotes character
        // with open and close the value or the data.

        char jChar = 'J';
        char oChar = 'o';
        char sChar = 's';
        char uChar = 'u';
        char aChar = 'a';

        // Use "println" to log the data after each line
        System.out.println("\nUse 'println' :");
        System.out.println(jChar);
        System.out.println(oChar);
        System.out.println(sChar);
        System.out.println(uChar);
        System.out.println(aChar);

        // Use "print" to log the data, display inline, without space or block line.
        System.out.println("\nUse only 'print' :");
        System.out.print(jChar);
        System.out.print(oChar);
        System.out.print(sChar);
        System.out.print(uChar);
        System.out.print(aChar);
    }
}
