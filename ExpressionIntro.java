public class ExpressionIntro {
    public static void main (String[] args) {
        // Expression is a single value
        int anExpression;
        anExpression = 7;

        // This line is a statement
        System.out.println(anExpression);

        // Statement with an argument
        // this argument is an expression
        System.out.println(anExpression = 4);

        // Expression is like a word in a sentence.
        // Statement can have one or many expression.

        System.out.println(anExpression = 22 / 7);
    }
}
